#include "cub3d.h"

void	ft_init(t_t *t)
{
	t->image = 0;
	t->demimap = 0;
	t->x = 0;
	t->y = 0;
	t->fy = -1;
	t->fx = -1;
	t->win_x = 0;
	t->win_y = 0;
	t->ccolor = 0;
	t->fcolor = 0;
	t->o = 0;
	t->maxy = 0;
	t->maxx = 0;
}
